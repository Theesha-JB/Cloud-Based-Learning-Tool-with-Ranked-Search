    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel model1 = new DefaultTableModel();
            model1.addColumn("Sl No");
            model1.addColumn("Book Name");
            model1.addColumn("Author Name");
            model1.addColumn("Publisher Name");
            model1.addColumn("Year");
            jTable2.setModel(model1);
            AESencrp aes = new AESencrp();
            for (int i = 0; i < opbn.size(); i++) {
                String wordDec1 = AESencrp.decrypt(opbn.get(i).toString(), datakey);
                String wordDec2 = AESencrp.decrypt(opan.get(i).toString(), datakey);
                String wordDec3 = AESencrp.decrypt(oppn.get(i).toString(), datakey);
                String wordDec4 = AESencrp.decrypt(opy.get(i).toString(), datakey);
                model1.insertRow(i, new Object[]{"", ""});
                model1.setValueAt((i + 1), i, 0);
                model1.setValueAt(wordDec1, i, 1);
                model1.setValueAt(wordDec2, i, 2);
                model1.setValueAt(wordDec3, i, 3);
                model1.setValueAt(wordDec4, i, 4);
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

