    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Socket s = new Socket("localhost", 1234);
            ObjectInputStream ois = new ObjectInputStream(s.getInputStream());
            opbn = (ArrayList) ois.readUnshared();
            opan = (ArrayList) ois.readUnshared();
            oppn = (ArrayList) ois.readUnshared();
            opy = (ArrayList) ois.readUnshared();
            alind = (ArrayList) ois.readUnshared();
            ois.close();
            s.close();
            DefaultTableModel model1 = new DefaultTableModel();
            model1.addColumn("Sl No");
            model1.addColumn("Book Name");
            model1.addColumn("Author Name");
            model1.addColumn("Publisher Name");
            model1.addColumn("Year");
            jTable1.setModel(model1);
            for (int i = 0; i < opan.size(); i++) {
                model1.insertRow(i, new Object[]{"", ""});
                model1.setValueAt(alind.get(i), i, 0);
                model1.setValueAt(opbn.get(i), i, 1);
                model1.setValueAt(opan.get(i), i, 2);
                model1.setValueAt(oppn.get(i), i, 3);
                model1.setValueAt(opy.get(i), i, 4);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

